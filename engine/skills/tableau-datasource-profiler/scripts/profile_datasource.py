def render_json(profile: Dict[str, Any]) -> str:
    return json.dumps(profile, indent=2, default=str)


def render_markdown(profile: Dict[str, Any]) -> str:
    header = profile["schema"]["header"]
    fields = profile["schema"]["fields"]
    stats = (profile.get("stats") or {}).get("fields") or {}
    row_count = (profile.get("stats") or {}).get("row_count")

    calc_count = sum(1 for f in fields if f.get("isCalculated"))
    measure_count = sum(1 for f in fields if (f.get("role") or "").lower() == "measure")
    dim_count = sum(1 for f in fields if (f.get("role") or "").lower() == "dimension")
    hidden_count = sum(1 for f in fields if f.get("isHidden"))

    lines: List[str] = []
    lines.append(f"# Datasource Profile: {header.get('name')}")
    lines.append("")
    lines.append(f"- **Project:** {header.get('projectName')}")
    lines.append(f"- **LUID:** {header.get('luid')}")
    lines.append(f"- **Certified:** {header.get('isCertified')}")
    lines.append(f"- **Has extracts:** {header.get('hasExtracts')}")
    lines.append(f"- **Contains unsupported custom SQL:** {header.get('containsUnsupportedCustomSql')}")
    lines.append(f"- **Has user reference (RLS/USERNAME):** {header.get('hasUserReference')}")
    if header.get("description"):
        lines.append(f"- **Description:** {header.get('description')}")
    lines.append("")
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- Total fields: {profile['schema'].get('field_count')}")
    lines.append(f"- Measures: {measure_count} | Dimensions: {dim_count} | Hidden: {hidden_count}")
    lines.append(f"- Calculated fields (DAX translation needed): **{calc_count}**")
    if row_count is not None:
        approx = " (approx; max non-null across fields)" if profile.get("stats", {}).get("row_count_approx") else ""
        lines.append(f"- Row count{approx}: {row_count}")
    lines.append("")

    has_stats = bool(stats)
    lines.append("## Fields")
    lines.append("")
    if has_stats:
        lines.append("| Field | Role | Type | Calc? | Cardinality | Null rate | Min | Max | Avg |")
        lines.append("|---|---|---|---|---|---|---|---|---|")
    else:
        lines.append("| Field | Role | Type | Calc? | Hidden | Folder |")
        lines.append("|---|---|---|---|---|---|")

    for f in fields:
        name = f.get("name") or ""
        role = f.get("role") or ""
        dtype = f.get("dataType") or ""
        calc = "yes" if f.get("isCalculated") else ""
        if has_stats:
            s = stats.get(name, {})
            lines.append(
                f"| {name} | {role} | {dtype} | {calc} | {_fmt(s.get('cardinality'))} "
                f"| {_fmt(s.get('null_rate'))} | {_fmt(s.get('min', s.get('min_date')))} "
                f"| {_fmt(s.get('max', s.get('max_date')))} | {_fmt(s.get('avg'))} |"
            )
        else:
            lines.append(
                f"| {name} | {role} | {dtype} | {calc} | {f.get('isHidden')} | {f.get('folderName') or ''} |"
            )

    calc_fields = [f for f in fields if f.get("isCalculated") and f.get("formula")]
    if calc_fields:
        lines.append("")
        lines.append("## Calculated Field Formulas")
        lines.append("")
        for f in calc_fields:
            lines.append(f"### {f.get('name')}")
            lines.append("")
            lines.append("```")
            lines.append((f.get("formula") or "").strip())
            lines.append("```")
            lines.append("")

    if (profile.get("stats") or {}).get("note"):
        lines.append(f"> {profile['stats']['note']}")
        lines.append("")
    lines.append(
        "> Value stats reflect only rows the PAT user is permitted to see (Tableau RLS). "
        "Use a Site Administrator PAT for complete data."
    )
    return "\n".join(lines)


def _fmt(value: Any) -> str:
    if value is None:
        return ""
    return str(value)



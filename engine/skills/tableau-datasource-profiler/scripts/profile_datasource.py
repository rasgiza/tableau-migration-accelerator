class TableauError(RuntimeError):
    pass


# --------------------------------------------------------------------------------------
# Connected App (Direct Trust) JWT — built with the standard library (HS256), so no
# extra dependency is required. Structure verified against Tableau's official sign-in
# reference (rest_api_concepts_auth.htm): header {alg, kid, iss}; claims
# {aud:"tableau", sub, scp, iss, exp, jti}. Signed with the connected app secret value.
# --------------------------------------------------------------------------------------
# Tableau Pulse REST scopes (metric definitions/metrics, subscriptions, and the
# AI-powered insight bundle/brief endpoints).
PULSE_JWT_SCOPES = [
    "tableau:insight_definitions_metrics:read",
    "tableau:insight_metrics:read",
    "tableau:metric_subscriptions:read",
    "tableau:insights:read",
    "tableau:insight_brief:create",
]
# Core scopes the datasource tools (REST + Metadata + VDS) need. Kept minimal so they
# never depend on Pulse being licensed/enabled.
CORE_JWT_SCOPES = ["tableau:content:read", "tableau:viz_data_service:read"]
# Attempted by default: core + Pulse, so Pulse works out of the box. connect_from_env
# falls back to CORE_JWT_SCOPES if a site rejects the Pulse scopes at sign-in, so adding
# Pulse can never regress the datasource tools.
DEFAULT_JWT_SCOPES = [*CORE_JWT_SCOPES, *PULSE_JWT_SCOPES]
JWT_MAX_TTL_SECONDS = 600  # Tableau rejects connected-app JWTs with exp > ~10 minutes.


def _b64url(raw: bytes) -> str:
    return base64.urlsafe_b64encode(raw).rstrip(b"=").decode("ascii")


def build_connected_app_jwt(
    client_id: str,
    secret_id: str,
    secret_value: str,
    username: str,
    scopes: Optional[List[str]] = None,
    ttl_seconds: int = 300,
) -> str:
    """Build a signed HS256 JWT for Tableau Connected App (Direct Trust) sign-in.

    `client_id`  -> connected app Client ID (JWT `iss` + header context)
    `secret_id`  -> connected app Secret ID (JWT header `kid`)
    `secret_value` -> connected app Secret Value (HS256 signing key)
    `username`   -> the Tableau user to act as (JWT `sub`); enables impersonation.
    `scopes`     -> JWT-supported access scopes (JWT `scp`).
    """
    if not all([client_id, secret_id, secret_value, username]):
        raise TableauError(
            "JWT auth requires client_id, secret_id, secret_value, and username."
        )
    ttl = max(1, min(int(ttl_seconds), JWT_MAX_TTL_SECONDS))
    header = {"alg": "HS256", "kid": secret_id, "iss": client_id}
    now = int(time.time())
    payload = {
        "iss": client_id,
        "aud": "tableau",
        "sub": username,
        "scp": list(scopes) if scopes else list(DEFAULT_JWT_SCOPES),
        "exp": now + ttl,
        "jti": str(uuid.uuid4()),
    }
    signing_input = (
        _b64url(json.dumps(header, separators=(",", ":")).encode("utf-8"))
        + "."
        + _b64url(json.dumps(payload, separators=(",", ":")).encode("utf-8"))
    )
    signature = hmac.new(
        secret_value.encode("utf-8"), signing_input.encode("ascii"), hashlib.sha256
    ).digest()
    return signing_input + "." + _b64url(signature)


class VDSRateLimit(TableauError):
    """Raised on HTTP 429 from VDS (100 calls/hour per Creator exceeded)."""
    pass


class PulseUnavailable(TableauError):
    """Raised when Tableau Pulse (or its AI insights) is not enabled for the site."""
    pass


class TableauClient:
    """Minimal direct-REST Tableau client (Cloud + Server compatible)."""

    def __init__(self, server: str, site_content_url: str, rest_version: str) -> None:
        self.server = server.rstrip("/")
        self.site_content_url = site_content_url
        self.rest_version = rest_version
        self.token: Optional[str] = None
        self.site_id: Optional[str] = None
        self.vds_calls = 0
        # Set False if the site rejected the Pulse scopes at sign-in (Pulse tools then
        # report a clear "not enabled / scopes unavailable" error instead of failing raw).
        self.pulse_scopes_granted = True

    # -- auth ---------------------------------------------------------------------------
    @property
    def _rest_base(self) -> str:
        return f"{self.server}/api/{self.rest_version}"

    def _auth_headers(self) -> Dict[str, str]:
        if not self.token:
            raise TableauError("Not signed in.")
        return {
            "X-Tableau-Auth": self.token,
            "Content-Type": "application/json",
            "Accept": "application/json",
        }

    def sign_in(self, pat_name: str, pat_value: str) -> None:
        _require_requests()
        body = {
            "credentials": {
                "site": {"contentUrl": self.site_content_url},
                "personalAccessTokenName": pat_name,
                "personalAccessTokenSecret": pat_value,
            }
        }
        self._complete_signin(body)

    def sign_in_jwt(
        self,
        client_id: str,
        secret_id: str,
        secret_value: str,
        username: str,
        scopes: Optional[List[str]] = None,
    ) -> None:
        """Sign in via a Connected App (Direct Trust) JWT, acting as `username`."""
        _require_requests()
        jwt = build_connected_app_jwt(
            client_id, secret_id, secret_value, username, scopes
        )
        body = {
            "credentials": {
                "jwt": jwt,
                "site": {"contentUrl": self.site_content_url},
            }
        }
        self._complete_signin(body)

    def _complete_signin(self, body: Dict[str, Any]) -> None:
        resp = requests.post(
            f"{self._rest_base}/auth/signin",
            data=json.dumps(body),
            headers={"Content-Type": "application/json", "Accept": "application/json"},
            timeout=60,
        )
        if resp.status_code != 200:
            raise TableauError(f"Sign-in failed ({resp.status_code}): {resp.text[:500]}")
        try:
            creds = resp.json()["credentials"]
            self.token = creds["token"]
            self.site_id = creds["site"]["id"]
        except (ValueError, KeyError, TypeError):
            raise TableauError(f"Unexpected sign-in response: {resp.text[:500]}")

    def sign_out(self) -> None:
        if not self.token:
            return
        try:
            requests.post(
                f"{self._rest_base}/auth/signout",
                headers=self._auth_headers(),
                timeout=30,
            )
        finally:
            self.token = None
            self.site_id = None

    # -- datasource resolution ----------------------------------------------------------
    def list_datasources(self, page_size: int = 100) -> List[Dict[str, Any]]:
        """Return published datasources on the site as [{luid, name, project}], paged."""
        out: List[Dict[str, Any]] = []
        page = 1
        while True:
            url = f"{self._rest_base}/sites/{self.site_id}/datasources"
            params = {"pageSize": str(page_size), "pageNumber": str(page)}
            resp = requests.get(url, headers=self._auth_headers(), params=params, timeout=60)
            if resp.status_code != 200:
                raise TableauError(
                    f"List datasources failed ({resp.status_code}): {resp.text[:500]}"
                )
            body = resp.json()
            for d in body.get("datasources", {}).get("datasource", []):
                out.append({
                    "luid": d.get("id", ""),
                    "name": d.get("name", ""),
                    "project": (d.get("project") or {}).get("name", ""),
                })
            total = int(body.get("pagination", {}).get("totalAvailable", len(out)) or len(out))
            if page * page_size >= total or not body.get("datasources", {}).get("datasource"):
                break
            page += 1
        return out

    def resolve_datasource_luid(self, name: str) -> Tuple[str, str]:
        """Return (luid, name) for the published datasource matching `name`."""
        url = f"{self._rest_base}/sites/{self.site_id}/datasources"
        params = {"filter": f"name:eq:{name}", "pageSize": "100"}
        resp = requests.get(url, headers=self._auth_headers(), params=params, timeout=60)
        if resp.status_code != 200:
            raise TableauError(
                f"List datasources failed ({resp.status_code}): {resp.text[:500]}"
            )
        datasources = resp.json().get("datasources", {}).get("datasource", [])
        if not datasources:
            raise TableauError(f"No published datasource found named '{name}'.")
        if len(datasources) > 1:
            names = ", ".join(d.get("name", "?") for d in datasources)
            raise TableauError(
                f"Multiple datasources matched '{name}': {names}. Use --datasource-luid."
            )
        ds = datasources[0]
        return ds["id"], ds.get("name", name)

    # -- metadata api -------------------------------------------------------------------
    def metadata_query(self, query: str, variables: Dict[str, Any]) -> Dict[str, Any]:
        url = f"{self.server}/api/metadata/graphql"
        resp = requests.post(
            url,
            data=json.dumps({"query": query, "variables": variables}),
            headers=self._auth_headers(),
            timeout=120,
        )
        if resp.status_code != 200:
            raise TableauError(
                f"Metadata API failed ({resp.status_code}): {resp.text[:500]}"
            )
        payload = resp.json()
        if payload.get("errors"):
            raise TableauError(f"Metadata API errors: {json.dumps(payload['errors'])[:500]}")
        return payload["data"]

    # -- vizql data service -------------------------------------------------------------
    @property
    def _vds_base(self) -> str:
        return f"{self.server}/api/v1/vizql-data-service"

    def vds_read_metadata(self, luid: str) -> Optional[List[Dict[str, Any]]]:
        body = {"datasource": {"datasourceLuid": luid}}
        data = self._vds_post("/read-metadata", body)
        if data is None:
            return None
        return data.get("data", [])

    def vds_query(self, luid: str, query: Dict[str, Any],
                  options: Optional[Dict[str, Any]] = None) -> Optional[List[Dict[str, Any]]]:
        body: Dict[str, Any] = {"datasource": {"datasourceLuid": luid}, "query": query}
        if options:
            body["options"] = options
        data = self._vds_post("/query-datasource", body)
        if data is None:
            return None
        return data.get("data", [])

    def _vds_post(self, path: str, body: Dict[str, Any]) -> Optional[Dict[str, Any]]:
        self.vds_calls += 1
        resp = requests.post(
            f"{self._vds_base}{path}",
            data=json.dumps(body),
            headers=self._auth_headers(),
            timeout=180,
        )
        if resp.status_code == 404:
            # VDS not enabled / Tableau < 2025.1 -> feature disabled.
            return None
        if resp.status_code == 429:
            raise VDSRateLimit(
                "VDS rate limit hit (429): 100 calls/hour per Creator exceeded."
            )
        if resp.status_code != 200:
            raise TableauError(
                f"VDS {path} failed ({resp.status_code}): {resp.text[:500]}"
            )
        try:
            payload = resp.json()
        except ValueError:
            raise TableauError(f"VDS {path} returned non-JSON: {resp.text[:500]}")
        # VDS can return HTTP 200 with an error envelope after partial streaming.
        if isinstance(payload, dict) and payload.get("error"):
            raise TableauError(f"VDS {path} error: {json.dumps(payload['error'])[:500]}")
        return payload

    # -- tableau pulse ------------------------------------------------------------------
    @property
    def _pulse_base(self) -> str:
        return f"{self.server}/api/-/pulse"

    @staticmethod
    def _pulse_insights_disabled(headers: Dict[str, str], text: str) -> bool:
        # These Tableau error codes mean AI-powered Pulse insights are disabled.
        codes = ("0x62c06627", "0x8c454877")
        blob = ((headers.get("tableau_error_code") or "") + " " + (text or "")).lower()
        return any(c in blob for c in codes)

    def _pulse_request(
        self,
        method: str,
        path: str,
        body: Optional[Dict[str, Any]] = None,
        params: Optional[Dict[str, Any]] = None,
    ) -> Dict[str, Any]:
        """Call a Tableau Pulse REST endpoint, mapping 'disabled' states to PulseUnavailable."""
        _require_requests()
        url = f"{self._pulse_base}{path}"
        resp = requests.request(
            method.upper(),
            url,
            headers=self._auth_headers(),
            params=params,
            data=json.dumps(body) if body is not None else None,
            timeout=180,
        )
        headers = {k.lower(): v for k, v in resp.headers.items()}
        if resp.status_code == 404:
            raise PulseUnavailable(
                "Tableau Pulse is not available on this site (404). It requires Tableau "
                "Cloud with Pulse enabled."
            )
        if resp.status_code == 400 and headers.get("tableau_error_code") == "0xd3408984":
            raise PulseUnavailable("Tableau Pulse is disabled for this site.")
        if self._pulse_insights_disabled(headers, resp.text):
            raise PulseUnavailable(
                "Tableau Pulse AI insights are disabled for this site. Enable Pulse "
                "insights (AI features) in Tableau Cloud settings to use this tool."
            )
        if resp.status_code == 429:
            raise VDSRateLimit("Tableau Pulse rate limit hit (429).")
        if not (200 <= resp.status_code < 300):
            raise TableauError(
                f"Pulse {path} failed ({resp.status_code}): {resp.text[:500]}"
            )
        try:
            return resp.json()
        except ValueError:
            raise TableauError(f"Pulse {path} returned non-JSON: {resp.text[:500]}")

    def pulse_list_all_definitions(
        self, view: str = "DEFINITION_VIEW_DEFAULT", page_size: int = 100
    ) -> List[Dict[str, Any]]:
        """Return all published Pulse metric definitions (paged), each with its default metric."""
        out: List[Dict[str, Any]] = []
        page_token: Optional[str] = None
        while True:
            params: Dict[str, Any] = {"view": view, "page_size": str(page_size)}
            if page_token:
                params["page_token"] = page_token
            data = self._pulse_request("GET", "/definitions", params=params)
            out.extend(data.get("definitions", []) or [])
            page_token = data.get("next_page_token") or ""
            if not page_token:
                break
        return out

    def pulse_get_definition(
        self, definition_id: str, view: str = "DEFINITION_VIEW_DEFAULT"
    ) -> Optional[Dict[str, Any]]:
        """Return one Pulse metric definition (with its default metric) by definition ID."""
        data = self._pulse_request(
            "POST",
            "/definitions:batchGet",
            body={"definition_ids": [definition_id]},
            params={"view": view},
        )
        defs = data.get("definitions", []) or []
        return defs[0] if defs else None

    def pulse_resolve_metric(
        self, metric: str
    ) -> Tuple[Optional[Dict[str, Any]], Optional[Dict[str, Any]]]:
        """Resolve a metric name OR definition/metric ID to (definition, default_metric).

        Returns (None, None) if nothing matches. Matching is case-insensitive on the
        definition name; IDs are matched against the definition id, default metric id.
        """
        needle = (metric or "").strip()
        if not needle:
            return None, None
        definitions = self.pulse_list_all_definitions()
        lowered = needle.lower()
        for d in definitions:
            md = d.get("metadata", {}) or {}
            m = pulse_default_metric(d)
            metric_id = (m or {}).get("id", "")
            if (
                (md.get("name", "").lower() == lowered)
                or (md.get("id", "") == needle)
                or (metric_id and metric_id == needle)
            ):
                return d, m
        return None, None

    def pulse_generate_insight_bundle(
        self, bundle_request: Dict[str, Any], bundle_type: str = "ban"
    ) -> Dict[str, Any]:
        """POST a Pulse insight bundle request; bundle_type in ban|springboard|basic|detail."""
        return self._pulse_request(
            "POST", f"/insights/{bundle_type}", body=bundle_request
        )

    def pulse_generate_insight_brief(self, brief_request: Dict[str, Any]) -> Dict[str, Any]:
        """POST a Pulse insight-brief request (AI conversational answer about metrics)."""
        return self._pulse_request("POST", "/insights/brief", body=brief_request)

    def pulse_list_subscriptions(self, user_id: str) -> List[Dict[str, Any]]:
        """Return the current user's Pulse metric subscriptions."""
        data = self._pulse_request(
            "GET", "/subscriptions", params={"user_id": user_id}
        )
        return data.get("subscriptions", []) or []


# --------------------------------------------------------------------------------------
# Tableau Pulse payload assembly (server-side, so an agent only needs a metric name).
# The insight bundle/brief endpoints require the full metric definition + specification
# in the request body; we build those from a fetched definition + its default metric.
# --------------------------------------------------------------------------------------
def pulse_default_metric(definition: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    """Return the default metric embedded in a Pulse definition (view=DEFAULT/FULL)."""
    metrics = definition.get("metrics", []) or []
    for m in metrics:
        if m.get("is_default"):
            return m
    return metrics[0] if metrics else None


def assemble_pulse_bundle_request(
    definition: Dict[str, Any],
    metric: Dict[str, Any],
    output_format: str = "OUTPUT_FORMAT_HTML",
    time_zone: str = "UTC",
    language: str = "LANGUAGE_EN_US",
    locale: str = "LOCALE_EN_US",
) -> Dict[str, Any]:
    """Build the body for POST /pulse/insights/{bundle_type} from a definition + metric."""
    spec = definition.get("specification", {}) or {}
    md = definition.get("metadata", {}) or {}
    metric_input: Dict[str, Any] = {
        "definition": {
            "datasource": {"id": (spec.get("datasource", {}) or {}).get("id", "")},
            "basic_specification": spec.get("basic_specification", {}) or {},
            "is_running_total": bool(spec.get("is_running_total", False)),
        },
        "metric_specification": metric.get("specification", {}) or {},
        "extension_options": definition.get("extension_options", {}) or {},
        "representation_options": definition.get("representation_options", {}) or {},
        "insights_options": definition.get("insights_options", {}) or {},
    }
    if metric.get("goals"):
        metric_input["goals"] = metric["goals"]
    return {
        "bundle_request": {
            "version": 1,
            "options": {
                "output_format": output_format,
                "time_zone": time_zone,
                "language": language,
                "locale": locale,
            },
            "input": {
                "metadata": {
                    "name": md.get("name", ""),
                    "metric_id": metric.get("id", ""),
                    "definition_id": md.get("id", ""),
                },
                "metric": metric_input,
            },
        }
    }


def assemble_pulse_metric_group_context(
    definition: Dict[str, Any], metric: Dict[str, Any]
) -> Dict[str, Any]:
    """Build one metric_group_context entry for a Pulse insight-brief request."""
    spec = definition.get("specification", {}) or {}
    md = definition.get("metadata", {}) or {}
    ctx: Dict[str, Any] = {
        "metadata": {
            "name": md.get("name", ""),
            "metric_id": metric.get("id", ""),
            "definition_id": md.get("id", ""),
        },
        "metric": {
            "definition": spec,
            "metric_specification": metric.get("specification", {}) or {},
            "extension_options": definition.get("extension_options", {}) or {},
            "representation_options": definition.get("representation_options", {}) or {},
            "insights_options": definition.get("insights_options", {}) or {},
            "candidates": [],
        },
    }
    goals: Dict[str, Any] = {}
    if definition.get("datasource_goals"):
        goals["datasource_goals"] = definition["datasource_goals"]
    if metric.get("goals"):
        goals["metric_goals"] = metric["goals"]
    if goals:
        ctx["metric"]["goals"] = goals
    return ctx



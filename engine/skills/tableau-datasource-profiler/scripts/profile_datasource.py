def parse_args(argv: Optional[List[str]] = None) -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Profile a published Tableau datasource (schema via Metadata API; "
                    "optional value stats via VizQL Data Service).",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    target = p.add_mutually_exclusive_group(required=True)
    target.add_argument("--datasource-name", help="Published datasource name (resolved to LUID via REST).")
    target.add_argument("--datasource-luid", help="Published datasource LUID (skips name resolution).")
    p.add_argument("--with-stats", action="store_true",
                   help="Also compute value-level stats via VDS (rate-limited; batched).")
    p.add_argument("--format", choices=["md", "json"], default="md", help="Output format (default: md).")
    p.add_argument("--out", help="Write output to this file instead of stdout.")
    p.add_argument("--dry-run", action="store_true",
                   help="Print the requests that would be sent; make no network calls.")
    p.add_argument("--page-size", type=int, default=200, help="GraphQL fieldsConnection page size (default 200).")
    p.add_argument("--max-fields-per-query", type=int, default=100,
                   help="Max fields per VDS query before chunking (default 100).")
    p.add_argument("--auth", choices=["pat", "jwt"], default=os.environ.get("TABLEAU_AUTH", "pat"),
                   help="Auth mode: 'pat' (default) or 'jwt' (Connected App Direct Trust).")
    p.add_argument("--jwt-username",
                   help="User to act as for JWT auth (overrides TABLEAU_JWT_USERNAME). "
                        "Use a Site Admin to bypass RLS partial-data.")
    p.add_argument("--rest-version", default=os.environ.get("TABLEAU_REST_VERSION", DEFAULT_REST_VERSION),
                   help=f"Tableau REST API version (default {DEFAULT_REST_VERSION}).")
    return p.parse_args(argv)


def _jwt_scopes_from_env() -> Optional[List[str]]:
    raw = os.environ.get("TABLEAU_JWT_SCOPES", "").strip()
    if not raw:
        return None
    parts = [s.strip() for s in raw.replace(",", " ").split()]
    return [s for s in parts if s] or None


def connect_from_env(
    rest_version: str, auth: str = "pat", jwt_username: Optional[str] = None
) -> "TableauClient":
    """Build and sign in a TableauClient using environment variables.

    Shared by the profiler and the query tool (and a future MCP server). Raises
    TableauError listing any missing variables. `auth` is 'pat' (default) or 'jwt'.
    """
    _require_requests()
    server = os.environ.get("TABLEAU_SERVER", "")
    site = os.environ.get("TABLEAU_SITE", "")
    auth = (auth or "pat").lower()
    if not server:
        raise TableauError("missing required env var: TABLEAU_SERVER")
    client = TableauClient(server, site, rest_version)
    if auth == "jwt":
        cid = os.environ.get("TABLEAU_CONNECTED_APP_CLIENT_ID", "")
        sid = os.environ.get("TABLEAU_CONNECTED_APP_SECRET_ID", "")
        sval = os.environ.get("TABLEAU_CONNECTED_APP_SECRET_VALUE", "")
        user = jwt_username or os.environ.get("TABLEAU_JWT_USERNAME", "")
        missing = [n for n, v in [
            ("TABLEAU_CONNECTED_APP_CLIENT_ID", cid),
            ("TABLEAU_CONNECTED_APP_SECRET_ID", sid),
            ("TABLEAU_CONNECTED_APP_SECRET_VALUE", sval),
            ("TABLEAU_JWT_USERNAME (or --jwt-username)", user),
        ] if not v]
        if missing:
            raise TableauError(f"missing required env var(s): {', '.join(missing)}")
        override = _jwt_scopes_from_env()
        if override is not None:
            # Operator pinned an explicit scope set; respect it verbatim.
            client.sign_in_jwt(cid, sid, sval, user, override)
            client.pulse_scopes_granted = all(s in override for s in PULSE_JWT_SCOPES)
        else:
            # Try core + Pulse; if the site rejects the Pulse scopes, fall back to core
            # so the datasource tools still work (Pulse tools then report unavailable).
            try:
                client.sign_in_jwt(cid, sid, sval, user, DEFAULT_JWT_SCOPES)
                client.pulse_scopes_granted = True
            except TableauError:
                client.sign_in_jwt(cid, sid, sval, user, CORE_JWT_SCOPES)
                client.pulse_scopes_granted = False
    else:
        pat_name = os.environ.get("TABLEAU_PAT_NAME", "")
        pat_value = os.environ.get("TABLEAU_PAT_VALUE", "")
        missing = [n for n, v in [
            ("TABLEAU_PAT_NAME", pat_name), ("TABLEAU_PAT_VALUE", pat_value),
        ] if not v]
        if missing:
            raise TableauError(f"missing required env var(s): {', '.join(missing)}")
        client.sign_in(pat_name, pat_value)
    return client


def main(argv: Optional[List[str]] = None) -> int:
    args = parse_args(argv)
    args.auth = (args.auth or "pat").lower()

    server = os.environ.get("TABLEAU_SERVER", "")
    site = os.environ.get("TABLEAU_SITE", "")
    pat_name = os.environ.get("TABLEAU_PAT_NAME", "")
    pat_value = os.environ.get("TABLEAU_PAT_VALUE", "")

    # Connected App (Direct Trust) JWT auth inputs.
    ca_client_id = os.environ.get("TABLEAU_CONNECTED_APP_CLIENT_ID", "")
    ca_secret_id = os.environ.get("TABLEAU_CONNECTED_APP_SECRET_ID", "")
    ca_secret_value = os.environ.get("TABLEAU_CONNECTED_APP_SECRET_VALUE", "")
    jwt_username = args.jwt_username or os.environ.get("TABLEAU_JWT_USERNAME", "")
    jwt_scopes = _jwt_scopes_from_env()

    if args.dry_run:
        text = do_dry_run(args, server or "https://<your-tableau-server>", site, args.rest_version)
        _emit(text, args.out)
        return 0

    if args.auth == "jwt":
        if jwt_scopes is not None:
            required_scopes = {"tableau:content:read"}
            if args.with_stats:
                required_scopes.add("tableau:viz_data_service:read")
            missing_scopes = sorted(required_scopes - set(jwt_scopes))
            if missing_scopes:
                sys.stderr.write(
                    "WARNING: TABLEAU_JWT_SCOPES is missing recommended scope(s): "
                    f"{', '.join(missing_scopes)}. Sign-in may succeed but content or "
                    "value-stats calls could fail.\n"
                )
        required = [
            ("TABLEAU_SERVER", server),
            ("TABLEAU_CONNECTED_APP_CLIENT_ID", ca_client_id),
            ("TABLEAU_CONNECTED_APP_SECRET_ID", ca_secret_id),
            ("TABLEAU_CONNECTED_APP_SECRET_VALUE", ca_secret_value),
            ("TABLEAU_JWT_USERNAME (or --jwt-username)", jwt_username),
        ]
    else:
        required = [
            ("TABLEAU_SERVER", server),
            ("TABLEAU_PAT_NAME", pat_name),
            ("TABLEAU_PAT_VALUE", pat_value),
        ]
    missing = [n for n, v in required if not v]
    if missing:
        sys.stderr.write(f"ERROR: missing required env var(s): {', '.join(missing)}\n")
        return 2

    client = TableauClient(server, site, args.rest_version)
    try:
        if args.auth == "jwt":
            client.sign_in_jwt(ca_client_id, ca_secret_id, ca_secret_value, jwt_username, jwt_scopes)
        else:
            client.sign_in(pat_name, pat_value)

        if args.datasource_luid:
            luid, ds_name = args.datasource_luid, None
        else:
            luid, ds_name = client.resolve_datasource_luid(args.datasource_name)

        schema = build_schema_profile(client, luid, args.page_size)
        if ds_name and not schema["header"].get("name"):
            schema["header"]["name"] = ds_name

        profile: Dict[str, Any] = {"schema": schema}

        if args.with_stats:
            try:
                stats = run_vds_stats(client, luid, args.max_fields_per_query)
            except VDSRateLimit as exc:
                stats = {
                    "fields": {},
                    "note": f"{exc} Showing schema profile only; retry value stats later.",
                }
            if stats is None:
                profile["stats"] = {
                    "note": "VizQL Data Service is disabled or unavailable for this datasource "
                            "(requires Tableau 2025.1+ with VDS enabled). Schema profile only.",
                    "fields": {},
                }
            else:
                profile["stats"] = stats

        text = render_json(profile) if args.format == "json" else render_markdown(profile)
        _emit(text, args.out)
        return 0
    except TableauError as exc:
        sys.stderr.write(f"ERROR: {exc}\n")
        return 1
    finally:
        client.sign_out()


def _emit(text: str, out_path: Optional[str]) -> None:
    if out_path:
        with open(out_path, "w", encoding="utf-8") as fh:
            fh.write(text)
        sys.stderr.write(f"Wrote {out_path}\n")
    else:
        sys.stdout.write(text + "\n")


if __name__ == "__main__":
    raise SystemExit(main())

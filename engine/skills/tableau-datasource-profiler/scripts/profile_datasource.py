# Schema profiling
# --------------------------------------------------------------------------------------
def build_schema_profile(client: TableauClient, luid: str, page_size: int) -> Dict[str, Any]:
    header: Dict[str, Any] = {}
    fields: List[Dict[str, Any]] = []
    cursor: Optional[str] = None
    total: Optional[int] = None

    while True:
        data = client.metadata_query(
            SCHEMA_GRAPHQL, {"luid": luid, "first": page_size, "after": cursor}
        )
        dslist = data.get("publishedDatasources", [])
        if not dslist:
            raise TableauError(f"No published datasource found with luid '{luid}'.")
        ds = dslist[0]
        if not header:
            header = {
                "id": ds.get("id"),
                "name": ds.get("name"),
                "luid": ds.get("luid"),
                "projectName": ds.get("projectName"),
                "isCertified": ds.get("isCertified"),
                "hasExtracts": ds.get("hasExtracts"),
                "hasUserReference": ds.get("hasUserReference"),
                "containsUnsupportedCustomSql": ds.get("containsUnsupportedCustomSql"),
                "description": ds.get("description"),
            }
        conn = ds.get("fieldsConnection", {}) or {}
        total = conn.get("totalCount", total)
        for node in conn.get("nodes", []) or []:
            fields.append(_normalize_field(node))
        page_info = conn.get("pageInfo", {}) or {}
        if page_info.get("hasNextPage") and page_info.get("endCursor"):
            cursor = page_info["endCursor"]
        else:
            break

    return {"header": header, "fields": fields, "field_count": total if total is not None else len(fields)}


def _normalize_field(node: Dict[str, Any]) -> Dict[str, Any]:
    typename = node.get("__typename")
    is_calculated = typename == "CalculatedField"
    upstream_tables = [t.get("name") for t in node.get("upstreamTables", []) or [] if t.get("name")]
    referenced_columns = [c.get("name") for c in node.get("columns", []) or [] if c.get("name")]
    return {
        "name": node.get("name"),
        "typename": typename,
        "role": node.get("role"),
        "dataType": node.get("dataType"),
        "isHidden": node.get("isHidden"),
        "folderName": node.get("folderName"),
        "description": node.get("description"),
        "isCalculated": is_calculated,
        "formula": node.get("formula") if is_calculated else None,
        "isAutoGenerated": node.get("isAutoGenerated"),
        "upstreamTables": upstream_tables,
        "referencedColumns": referenced_columns,
    }


# --------------------------------------------------------------------------------------
# VDS value stats (optional, adaptive batching)
# --------------------------------------------------------------------------------------
# A valid VDS aggregate query returns exactly one row. Some fields cannot be aggregated
# the way we ask (bins and certain calcs 400; grouped fields are silently treated as a
# GROUP BY and return one row per member, corrupting every other field in the query). We
# therefore batch per function, and on a 400 *or* a multi-row response we split the batch
# and retry, isolating and dropping only the offending field(s).
def is_vds_aggregatable(field: Dict[str, Any]) -> bool:
    """Heuristic: skip fields VDS can't aggregate cleanly. Bins 400; grouped fields are
    silently treated as a GROUP BY. Both are detectable from the VDS fieldName suffix."""
    fname = (field.get("fieldName") or "").lower()
    return not (fname.endswith("(bin)") or "(group)" in fname or "(set)" in fname)


def build_agg_groups(vds_fields: List[Dict[str, Any]]) -> List[Tuple[str, str, List[str]]]:
    """Return (function, out_key, [captions]) groups for the stats we collect."""
    def caps(pred) -> List[str]:
        seen: Dict[str, None] = {}
        for f in vds_fields:
            c = f.get("fieldCaption")
            if c and pred(f.get("dataType")) and c not in seen:
                seen[c] = None
        return list(seen)

    numeric = caps(lambda t: t in NUMERIC_TYPES)
    dates = caps(lambda t: t in DATE_TYPES)
    dims = caps(lambda t: t not in NUMERIC_TYPES | DATE_TYPES)
    everything = caps(lambda t: True)

    groups = [
        ("COUNT", "non_null", everything),
        ("COUNTD", "cardinality", dims),
        ("MIN", "min", numeric),
        ("MAX", "max", numeric),
        ("AVG", "avg", numeric),
        ("MIN", "min_date", dates),
        ("MAX", "max_date", dates),
    ]
    return [g for g in groups if g[2]]


def estimate_vds_calls(vds_fields: List[Dict[str, Any]], max_fields: int) -> int:
    """Optimistic count: one read-metadata plus one query per full batch (no splits)."""
    groups = build_agg_groups(vds_fields)
    return 1 + sum(math.ceil(len(caps) / max_fields) for _, _, caps in groups)


def run_vds_stats(
    client: TableauClient, luid: str, max_fields: int, max_calls: int = 100
) -> Optional[Dict[str, Any]]:
    vds_fields = client.vds_read_metadata(luid)
    if vds_fields is None:
        return None  # feature disabled

    fields = [
        {
            "fieldCaption": f.get("fieldCaption"),
            "fieldName": f.get("fieldName"),
            "dataType": f.get("dataType"),
        }
        for f in vds_fields
        if f.get("fieldCaption")
    ]
    skipped_unaggregatable = [
        f["fieldCaption"] for f in fields if not is_vds_aggregatable(f)
    ]
    fields = [f for f in fields if is_vds_aggregatable(f)]

    estimated = estimate_vds_calls(fields, max_fields)
    if estimated > max_calls:
        return {
            "row_count": None,
            "fields": {},
            "vds_calls": client.vds_calls,
            "note": (
                f"Skipped VDS value stats: would need ~{estimated} calls, over the "
                f"{max_calls}/hr limit. Reduce field count or raise --max-fields-per-query."
            ),
        }

    stats: Dict[str, Dict[str, Any]] = {}
    warnings: List[str] = []
    budget = {"capped": False, "rate_limited": False, "stop": False}

    def run_chunk(function: str, out_key: str, caps: List[str]) -> None:
        if not caps or budget["stop"]:
            return
        if client.vds_calls >= max_calls:
            budget["capped"] = True
            budget["stop"] = True
            return
        body = {"fields": [
            {"fieldCaption": c, "function": function, "fieldAlias": f"a{i}"}
            for i, c in enumerate(caps)
        ]}
        try:
            rows = client.vds_query(luid, body)
        except VDSRateLimit:
            budget["rate_limited"] = True
            budget["stop"] = True
            return
        except TableauError as exc:
            if len(caps) == 1:
                warnings.append(f"{caps[0]} [{function}]: {str(exc)[:140]}")
                return
            mid = len(caps) // 2
            run_chunk(function, out_key, caps[:mid])
            run_chunk(function, out_key, caps[mid:])
            return
        if rows is None:
            return
        # Exactly one row == clean aggregate. More rows == a field acted as a GROUP BY.
        if len(rows) != 1:
            if len(caps) == 1:
                warnings.append(
                    f"{caps[0]} [{function}]: not aggregatable (returned {len(rows)} rows)"
                )
                return
            mid = len(caps) // 2
            run_chunk(function, out_key, caps[:mid])
            run_chunk(function, out_key, caps[mid:])
            return
        row = rows[0]
        for i, c in enumerate(caps):
            stats.setdefault(c, {})[out_key] = row.get(f"a{i}")

    for function, out_key, caps in build_agg_groups(fields):
        if budget["stop"]:
            break
        for i in range(0, len(caps), max_fields):
            run_chunk(function, out_key, caps[i : i + max_fields])

    # Row count is approximated as the max non-null count across fields (>= any single
    # non-null count), so null_count is never negative. VDS has no COUNT(*).
    non_nulls = {
        c: v["non_null"] for c, v in stats.items()
        if isinstance(v.get("non_null"), (int, float))
    }
    row_count = max(non_nulls.values()) if non_nulls else None
    if row_count:
        for c, nn in non_nulls.items():
            stats[c]["null_count"] = row_count - nn
            stats[c]["null_rate"] = round((row_count - nn) / row_count, 4)

    result: Dict[str, Any] = {
        "row_count": row_count,
        "row_count_approx": True,
        "fields": stats,
        "vds_calls": client.vds_calls,
    }
    notes: List[str] = []
    if budget["rate_limited"]:
        notes.append(
            "Stopped early: VDS returned 429 (100 calls/hour per Creator). "
            "Stats shown are partial; retry later."
        )
    if budget["capped"]:
        notes.append(f"Hit the {max_calls}-call VDS budget; some stats were skipped.")
    if skipped_unaggregatable:
        notes.append(
            f"{len(skipped_unaggregatable)} bin/group/set field(s) excluded from value "
            f"stats (not aggregatable in VDS): {', '.join(skipped_unaggregatable[:5])}"
            + ("…" if len(skipped_unaggregatable) > 5 else "")
        )
    if warnings:
        notes.append(
            f"{len(warnings)} field/aggregation combo(s) skipped as not aggregatable "
            f"(e.g. calculated measures). First: {warnings[0]}"
        )
    if notes:
        result["note"] = " ".join(notes)
    return result



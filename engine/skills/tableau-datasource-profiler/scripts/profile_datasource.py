def do_dry_run(args: argparse.Namespace, server: str, site: str, rest_version: str) -> str:
    luid = args.datasource_luid or "<resolved-luid>"
    out: List[str] = []
    out.append("# DRY RUN — no network calls will be made\n")
    out.append("## 1. Sign in (REST)")
    out.append(f"POST {server}/api/{rest_version}/auth/signin")
    if getattr(args, "auth", "pat") == "jwt":
        out.append("# Connected App (Direct Trust) JWT. Header {alg:HS256, kid:<secret-id>, "
                   "iss:<client-id>}; claims {aud:'tableau', sub:<username>, scp:[...], "
                   "iss:<client-id>, exp, jti}. Signed (HS256) with the connected app secret value.")
        out.append(json.dumps(
            {"credentials": {"jwt": "<signed-jwt>", "site": {"contentUrl": site}}}, indent=2))
    else:
        out.append(json.dumps(
            {"credentials": {"site": {"contentUrl": site}, "personalAccessTokenName": "<PAT_NAME>",
                             "personalAccessTokenSecret": "<PAT_VALUE>"}}, indent=2))
    if not args.datasource_luid:
        out.append("\n## 2. Resolve datasource LUID (REST)")
        out.append(f"GET {server}/api/{rest_version}/sites/<site-id>/datasources"
                   f"?filter=name:eq:{args.datasource_name}")
    out.append("\n## 3. Schema profile (Metadata API GraphQL)")
    out.append(f"POST {server}/api/metadata/graphql")
    out.append(json.dumps({"query": SCHEMA_GRAPHQL,
                           "variables": {"luid": luid, "first": args.page_size, "after": None}}, indent=2))
    if args.with_stats:
        out.append("\n## 4. Value stats (VizQL Data Service) — batched")
        out.append(f"POST {server}/api/v1/vizql-data-service/read-metadata")
        out.append(json.dumps({"datasource": {"datasourceLuid": luid}}, indent=2))
        out.append("Then batched POST .../query-datasource calls, one aggregate function per "
                   "query (VDS forbids repeating a field in a single query): per-field COUNT "
                   "(non-null -> null rates), dimension COUNTD (cardinality), numeric MIN, MAX, "
                   "AVG, and date MIN, MAX. Bin/group/set fields are excluded up front; any "
                   "batch that 400s or returns multiple rows (a field acting as GROUP BY) is "
                   "split and retried so one bad field doesn't sink the batch. Example numeric "
                   "MIN batch:")
        example = {
            "datasource": {"datasourceLuid": luid},
            "query": {"fields": [
                {"fieldCaption": "Sales", "function": "MIN", "fieldAlias": "a0"},
                {"fieldCaption": "Profit", "function": "MIN", "fieldAlias": "a1"},
            ]},
        }
        out.append(json.dumps(example, indent=2))
    out.append("\n## 5. Sign out (REST)")
    out.append(f"POST {server}/api/{rest_version}/auth/signout")
    return "\n".join(out)


